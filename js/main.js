$(document).ready(function(){



$(window).scroll(function() {

  setTimeout(function(){

    if($(window).width() > 900){
      if($(window).scrollTop() > 2500 ) {
        $('.valores').css("background", "transparent");
      } else{
        $('.valores').css("background", "#000");
      }
    }
  },400);
 




    if($(window).width() > 900){
      if($(window).scrollTop() > 2500 ) {
        $('.valor-cont p').css("color", "#414042");
      } else{
        $('.valor-cont p').css("color", "#fff");
      }
    }
 


    if($(window).width() > 900){
      if($(window).scrollTop() > 2500 ) {
        $('.plug1').css("left", "-25px");
      } else{
        $('.plug1').css("left", "-30%");
      }
    }
  


    if($(window).width() > 900){
      if($(window).scrollTop() > 2500 ) {
        $('.plug2').css("right", "0px");
      } else{
        $('.plug2').css("right", "-30%");
      }
    }
 




  setTimeout(function(){

    if($(window).width() > 900){
      if($(window).scrollTop() > 2500 ) {
        $('.servicios').css("background", "transparent");
      } else{
        $('.servicios').css("background", "#000");
      }
    }
  },400);
   



  setTimeout(function(){

    if($(window).width() > 900){
      if($(window).scrollTop() > 5300 ) {
        $('.servicios').css("background", "#000");
      }
    }
  },400);




 

    if($(window).width() > 900){
      if($(window).scrollTop() > 200 ) {
        $('nav').css("background", "rgba(0,0,0,0.5)");
      } else{
        $('nav').css("background", "rgba(0,0,0,0.2)");
      }
    }
	



    if($(window).width() > 900){
      if($(window).scrollTop() > 200 ) {
        $('.logo-home').css("opacity", "1");
      } else{
        $('.logo-home').css("opacity", "0");
      }
    }





    if($(window).width() > 900){
      if($(window).scrollTop() > 5300 ) {
        $('.plug4').css("left", "-30%");
      } else{
        $('.plug4').css("left", "-25px");
      }
    }




    if($(window).width() > 900){
      if($(window).scrollTop() > 5300 ) {
        $('.plug3').css("right", "-30%");
      } else{
        $('.plug3').css("right", "0px");
      }
    }





  setTimeout(function(){

    if($(window).width() > 900){
      if($(window).scrollTop() > 5300 ) {
        $('.contacto').css("background", "#000");
      } else{
        $('.contacto').css("background", "transparent");
      }
    }
  },400);




  setTimeout(function(){

    if($(window).width() > 900){
      if($(window).scrollTop() > 5300 ) {
        $('.iluminar2').css("background", "#000");
      } else{
        $('.iluminar2').css("background", "transparent");
      }
    }
  },400);


 }); 

  });


